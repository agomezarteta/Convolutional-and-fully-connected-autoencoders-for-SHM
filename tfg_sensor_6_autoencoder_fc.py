    i = i+1
    for data in train_data:
        # Asegúrate de que tus datos de entrada tengan la forma correcta
        data = data.view(1, -1)
        optimizer.zero_grad()
        outputs = autoencoder(data)
        loss = criterion(outputs, data)
        loss.backward()
        optimizer.step()
        epoch_v[i] = epoch
        loss_v[i] = loss.item()

