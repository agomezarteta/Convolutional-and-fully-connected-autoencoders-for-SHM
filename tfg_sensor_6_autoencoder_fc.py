class Autoencoder(nn.Module):
    def __init__(self):
        super(Autoencoder, self).__init__()
        # The encoding layers
        self.encoder = nn.Sequential(
            nn.Linear(7201, 800),
            nn.PReLU(),
            nn.Linear(800, 50),
            nn.PReLU(),
            nn.Linear(50, 8),
            nn.PReLU(),
        )
        # The decoding layers
        self.decoder = nn.Sequential(
            nn.Linear(8, 50),
            nn.PReLU(),
            nn.Linear(50, 800),
            nn.PReLU(),
            nn.Linear(800, 7201),
            nn.PReLU()
        )

    def forward(self, x):
        x = self.encoder(x)
        x = self.decoder(x)
        return x

